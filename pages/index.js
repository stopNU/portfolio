import { useForm, usePlugin, useCMS } from 'tinacms'

const pageData = {
  title: 'Tina is not a CMS',
  body: 'It is a toolkit for creating a custom CMS.',
};

function EditButton() {
  const cms = useCMS()
  return (
    <button onClick={() => cms.toggle()}>
      {cms.enabled ? 'Exit edit mode' : 'Edit this site'}
    </button>
  );
}

